    P_pi = np.prod(1 - 2 * lam)  # <Q_A>
    
    # Partial derivatives at n=1
    dn_P0 = np.sum(a_k)          # = -S(rho_A)
    dn_Ppi = P_pi * np.sum(r_k)
    
