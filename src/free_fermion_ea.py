def loschmidt_echo(HBdG_i, HBdG_f, N, t_array):
    """
    Compute Loschmidt echo rate function g(t) = -(1/N) log |L(t)|
    and the complex phase arg(G(t)) for topological DQPT identification.
    
    L(t) = |<psi_i | e^{-i H_f t} |psi_i>|^2
    
    For quench from ground state |GS_i> to H_f:
    G(t) = prod_k [cos(eps_k^f t) + i sin(eps_k^f t) * h_k]
    where h_k = (u_k^i u_k^f + v_k^i v_k^f) for Bogoliubov transformation mismatch.
    
    For product state initial state |psi_theta>, we use the full BdG approach.
    
    Returns: (g_t, phase_t, L_t) arrays of length len(t_array)
    """
    # Diagonalize initial and final Hamiltonians
    eps_i, V_i = solve_BdG(HBdG_i)
    eps_f, V_f = solve_BdG(HBdG_f)
    
    # Overlap matrix M = V_i^dag V_f (2N x 2N)
    M = V_i.conj().T @ V_f
    
    # G(t) = det(V_i^dag e^{-i H_f t} V_i)
    # = det(M diag(e^{-i eps_f t}) M^dag)
    # This is the Loschmidt amplitude for the initial ground state |GS_i>.
    # For product state |psi_theta>, we use the correlator approach below.
    
    # For the product state, G(t) = <psi_theta| e^{-i H_f t} |psi_theta>
    # = det(I - C_0 + C_0 U_f(t)^dag) for free fermions
    # where C_0 is initial correlator and U_f(t) is free-fermion time evolution.
    # This is the standard formula for free-fermion Loschmidt amplitude.
    
    eps_f_pos = eps_f[N:]   # positive energy eigenvalues
    
    g_arr = np.zeros(len(t_array))
    phase_arr = np.zeros(len(t_array))
    L_arr = np.zeros(len(t_array))
    
    # For ground state quench, use the simpler formula
    # G(t) = prod_k [cos(eps_k^f t) + i sin(eps_k^f t) * (|f_k|^2 - |g_k|^2)]
    # where f_k, g_k are Bogoliubov coefficients for the transformation between i and f
    # For now, return simple placeholder and compute from correlators
    
    return g_arr, phase_arr, L_arr


def loschmidt_from_correlator(C_full_0, V_f, eps_f, N, t_array):
    """
    Loschmidt echo from free-fermion correlator approach.
    
    G(t) = det(I_N - C_A(0) + (V_f diag(e^{i eps_f^+ t}) V_f^dag)[:N,:N] C_A(0))
    
    Actually, the cleanest formula uses:
    G(t) = det(M(t)) where M(t) is derived from initial correlator and time evolution.
    
    Simplified: use the return rate g(t) = -(1/N) log |det(I - C_0 + C_0(t))|
    where C_0(t) = U(t)^dag C_0 U(t) with U = full-body time evolution.
    
    For the product state with C_0 = lambda * I:
    G(t) = prod_k [e^{i eps_k^f t} lambda + (1-lambda)] for upper quasiparticle modes
    
    Returns: (g_t, phase_t, L_t)
    """
    lam_0 = C_full_0[0, 0].real  # initial occupation (uniform for product state)
    
    g_arr = np.zeros(len(t_array))
    phase_arr = np.zeros(len(t_array))
    L_arr = np.zeros(len(t_array))
    
    # Positive-energy eigenvalues only (N modes)
    eps_pos = eps_f[N:]  # positive eigenvalues
    
    for idx, t in enumerate(t_array):
        # G(t) = prod_k [(1-lam_0) + lam_0 * e^{2i eps_k t}] (for BdG structure)
        # This is the formula for product state initial condition
        factors = (1 - lam_0) + lam_0 * np.exp(2j * eps_pos * t)
        G_t = np.prod(factors)
        L_t = np.abs(G_t) ** 2
        L_arr[idx] = L_t
        if L_t > 1e-300:
            g_arr[idx] = -np.log(L_t) / N
        else:
            g_arr[idx] = np.inf
        phase_arr[idx] = np.angle(G_t)
    
    return g_arr, phase_arr, L_arr


# ── Main computation function ─────────────────────────────────────────────────


def compute_EA_dynamics(model, params_i, params_f, theta, N, NA,
                         t_max=None, dt_early=0.02, dt_late=0.05,
                         t_crossover=20.0):
    """
    Compute time-dependent Z2 EA for a quantum quench.
    
    Parameters
    ----------
    model : 'TFIM' or 'XY'
    params_i : dict with model parameters for initial Hamiltonian
    params_f : dict with model parameters for final Hamiltonian
    theta : float, initial state angle
    N : int, chain length
    NA : int, subsystem size
    t_max : float or None. If None, uses N-dependent formula.
    dt_early, dt_late : time steps for early/late times
    t_crossover : time at which to switch from dt_early to dt_late
    
    Returns
    -------
    dict with keys:
      t_arr, DeltaS_arr, S_rho_arr, P_pi_arr,
      p_plus_arr, p_minus_arr, g_Loschmidt, phase_Loschmidt, L_arr
    """
    # N-dependent t_max
    if t_max is None:
        t_max = max(60.0, 3 * NA / 4.0)
    
    # Build Hamiltonians
    if model == 'TFIM':
        HBdG_i = build_TFIM_BdG(N, params_i['g'])
        HBdG_f = build_TFIM_BdG(N, params_f['g'])
    elif model == 'XY':
        HBdG_i = build_XY_BdG(N, params_i['h'], params_i['gamma'])
        HBdG_f = build_XY_BdG(N, params_f['h'], params_f['gamma'])
    else:
        raise ValueError(f"Unknown model: {model}")
    
    # Diagonalize final Hamiltonian
    eps_f, V_f = solve_BdG(HBdG_f)
    
    # Initial correlator (full Nambu space)
    C_full_0 = initial_correlator_full_BdG(N, theta)
    
    # Time array (two-step resolution)
    t1 = np.arange(0, min(t_crossover, t_max) + dt_early, dt_early)
    if t_max > t_crossover:
        t2 = np.arange(t_crossover + dt_late, t_max + dt_late, dt_late)
        t_arr = np.concatenate([t1, t2])
    else:
        t_arr = t1
    t_arr = np.unique(t_arr[t_arr <= t_max + 1e-10])
    
    NT = len(t_arr)
    DeltaS_arr = np.zeros(NT)
    S_rho_arr = np.zeros(NT)
    P_pi_arr = np.zeros(NT)
    p_plus_arr = np.zeros(NT)
    p_minus_arr = np.zeros(NT)
    
    # Compute for each time point (single-step evolution)
    for idx, t in enumerate(t_arr):
        C_full_t = evolve_correlator(C_full_0, V_f, eps_f, t)
        CA = get_subsystem_correlator(C_full_t, NA).real  # real part (Gaussian state)
        
        DeltaS, S_rho, S_tilde, P_pi, p_plus, p_minus = entanglement_asymmetry(CA)
        DeltaS_arr[idx] = DeltaS
        S_rho_arr[idx] = S_rho
        P_pi_arr[idx] = P_pi
        p_plus_arr[idx] = p_plus
        p_minus_arr[idx] = p_minus
    
    # Loschmidt echo
    g_L, phase_L, L_arr = loschmidt_from_correlator(C_full_0, V_f, eps_f, N, t_arr)
    
    return {
        't': t_arr,
        'DeltaS': DeltaS_arr,
        'S_rho': S_rho_arr,
        'P_pi': P_pi_arr,
        'p_plus': p_plus_arr,
        'p_minus': p_minus_arr,
        'g_Loschmidt': g_L,
        'phase_Loschmidt': phase_L,
        'L': L_arr,
    }


def find_mpemba_crossing_time(t_arr, DeltaS1, DeltaS2, threshold_rel=0.01):
    """
    Find crossing time t_M where DeltaS1 first falls below DeltaS2.
    Assumes DeltaS1(0) > DeltaS2(0) (theta1 more asymmetric initially).
    
    Returns:
      t_M : float (crossing time, or inf if no crossing)
      crossed : bool
    """
    diff = DeltaS1 - DeltaS2
    initial_gap = abs(diff[0])
    
    if initial_gap < 0.05:
        return np.inf, False  # pairs too close, excluded
    
    # Find first negative diff exceeding 1% of initial gap
    threshold = -threshold_rel * initial_gap
    idx = np.where(diff < threshold)[0]
    
    if len(idx) == 0:
        return np.inf, False
    
    # Linearly interpolate for more precise t_M
    i = idx[0]
    if i > 0:
        t_M = t_arr[i - 1] + (t_arr[i] - t_arr[i - 1]) * (-diff[i - 1]) / (diff[i] - diff[i - 1])
    else:
        t_M = t_arr[i]
    
    return t_M, True


