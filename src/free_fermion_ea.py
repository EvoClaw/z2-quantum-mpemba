def benchmark_initial_EA(N, NA, theta, verbose=True):
    """
    Benchmark 1: Verify DeltaS_A(t=0) against closed-form formula.
    For uniform initial state: all lambda_k = sin^2(theta/2)
    
    Closed form:
      <Q_A> = cos^NA(theta)
      p+/- = (1 +/- cos^NA(theta)) / 2
      S(rho_A) = NA * h(sin^2(theta/2))
      S_+/- from S+/-_formula (see below)
    """
    lam = np.sin(theta / 2) ** 2
    CA = lam * np.eye(NA)
    
    # Numerical result
    DeltaS_num, S_rho_num, _, P_pi_num, p_plus_num, p_minus_num = entanglement_asymmetry(CA)
    
    # Analytical
    P_pi_ana = (1 - 2 * lam) ** NA
    p_plus_ana = (1 + P_pi_ana) / 2
    p_minus_ana = (1 - P_pi_ana) / 2
    S_rho_ana = NA * _binary_entropy(np.array([lam]))[0]
    
    # Symmetry-resolved entropies analytically (using uniform lambda)
    a_k = lam * np.log(lam) + (1 - lam) * np.log(1 - lam)
    if abs(lam - 0.5) < 1e-10:
        r_val = 1.0 - np.log(2.0)
    else:
        r_val = ((1 - lam) * np.log(1 - lam) - lam * np.log(lam)) / (1 - 2 * lam)
    
    dn_P0_ana = NA * a_k
    dn_Ppi_ana = P_pi_ana * NA * r_val
    
    if p_plus_ana > 1e-12:
        S_plus_ana = -(dn_P0_ana + dn_Ppi_ana) / (2 * p_plus_ana) + np.log(p_plus_ana)
    else:
        S_plus_ana = 0.0
    if p_minus_ana > 1e-12:
        S_minus_ana = -(dn_P0_ana - dn_Ppi_ana) / (2 * p_minus_ana) + np.log(p_minus_ana)
    else:
        S_minus_ana = 0.0
    
    H_pp_ana = (-p_plus_ana * np.log(max(p_plus_ana, 1e-15))
                - p_minus_ana * np.log(max(p_minus_ana, 1e-15)))
    S_tilde_ana = H_pp_ana + p_plus_ana * S_plus_ana + p_minus_ana * S_minus_ana
    DeltaS_ana = S_tilde_ana - S_rho_ana
    
    passed = abs(DeltaS_num - DeltaS_ana) < 1e-8
    if verbose:
        print(f"  Benchmark 1 (t=0 EA): theta={theta:.3f}, NA={NA}")
        print(f"    Numerical:   DeltaS={DeltaS_num:.8f}, P_pi={P_pi_num:.8f}")
        print(f"    Analytical:  DeltaS={DeltaS_ana:.8f}, P_pi={P_pi_ana:.8f}")
        print(f"    PASSED: {passed}")
    return passed


