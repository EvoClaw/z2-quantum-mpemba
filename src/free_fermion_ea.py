def benchmark_equipartition(N, NA, verbose=True):
    """
    Benchmark 3: At theta=pi/2 (lambda=1/2), P_pi=0, p+=p-=1/2,
    and S+/- = S(rho_A) - ln2 (equipartition).
    """
    theta = np.pi / 2
    lam = 0.5
    CA = lam * np.eye(NA)
    
    S_plus, S_minus, p_plus, p_minus = symmetry_resolved_entropies(CA)
    S_rho = entropy_from_correlator(CA)
    
    # Expected
    S_plus_expected = S_rho - np.log(2.0)
    S_minus_expected = S_rho - np.log(2.0)
    
    passed = (abs(p_plus - 0.5) < 1e-10 and
              abs(p_minus - 0.5) < 1e-10 and
              abs(S_plus - S_plus_expected) < 1e-8 and
              abs(S_minus - S_minus_expected) < 1e-8)
    if verbose:
        print(f"  Benchmark 3 (equipartition): p+={p_plus:.6f}, p-={p_minus:.6f}")
        print(f"    S+={S_plus:.6f}, S-={S_minus:.6f}, S_rho-ln2={S_rho-np.log(2):.6f}")
        print(f"    PASSED: {passed}")
    return passed


def benchmark_CFT_entropy(N, NA, verbose=True):
    """
    Benchmark 4: S(rho_A) at g=1 (TFIM critical) ~ (1/6) log NA + const.
    Check central charge c=1/2 for critical TFIM.
    """
    HBdG = build_TFIM_BdG(N, g=1.0)
    eps, V = solve_BdG(HBdG)
    
    # Ground state correlator: C_ij = <GS|c_i^dag c_j|GS> = (V_hole_pos) @ (V_hole_pos)^dag
    # where V_hole_pos = lower N rows of POSITIVE-energy eigenvectors (hole components)
    # This is correct from the Bogoliubov transformation: c_i = sum_{k>0}(U_{ki} gamma_k + V_{ki}* gamma_k^dag)
    # and C_ij = sum_{k>0} V_{ki} V_{kj}* = (V_hole_pos @ V_hole_pos^dag)_ij
    pos_idx = np.where(eps > 0)[0]
    V_hole_pos = V[N:, pos_idx]  # lower N rows, positive-energy eigenvectors
    C_GS = V_hole_pos @ V_hole_pos.conj().T
    
    NA_arr = np.array([NA // 4, NA // 2, NA])
    S_arr = []
    for na in NA_arr:
        CA = C_GS[:na, :na].real
        S_arr.append(entropy_from_correlator(CA))
    
    # Fit S ~ (c/3) log NA + const (CFT formula for periodic BC, subsystem = L)
    log_NA = np.log(NA_arr)
    # Simple linear regression
    p = np.polyfit(log_NA, S_arr, 1)
    c_measured = p[0] * 3  # slope = c/3
    
    passed = abs(c_measured - 0.5) < 0.05  # within 10% of c=1/2
    if verbose:
        print(f"  Benchmark 4 (CFT at g=1): c_measured={c_measured:.4f}, c_expected=0.5")
        print(f"    S at NA={NA_arr}: {[f'{s:.4f}' for s in S_arr]}")
        print(f"    PASSED: {passed}")
    return passed


def run_all_benchmarks(N=200, NA=20):
    """Run all validation benchmarks. Return True if all pass."""
    print("=" * 50)
    print("Running validation benchmarks")
    print("=" * 50)
    results = []
    results.append(benchmark_initial_EA(N, NA, theta=np.pi / 4))
    results.append(benchmark_initial_EA(N, NA, theta=np.pi / 3))
