def initial_correlator_full_BdG(N, theta):
    """
    Correlator in 2N-dimensional Nambu space for BdG time evolution.
    C_Nambu(0) = [[C(0), 0], [0, I - C(0)^T]] = [[lambda*I, 0], [0, (1-lambda)*I]]
    """
    lam = np.sin(theta / 2) ** 2
    C_full = np.zeros((2 * N, 2 * N), dtype=complex)
    C_full[:N, :N] = lam * np.eye(N)
    C_full[N:, N:] = (1 - lam) * np.eye(N)
    return C_full


# ── Time evolution ────────────────────────────────────────────────────────────


def evolve_correlator(C_full_0, V, eps, t):
    """
    Single-step time evolution of full Nambu correlator.
    C_full(t) = V diag(e^{-i eps_k t}) V^dag C_full(0) V diag(e^{i eps_k t}) V^dag

    NO step-by-step accumulation (prevents orthogonality drift).

    Returns: 2N x 2N complex matrix.
    """
    phase = np.exp(-1j * eps * t)
    # V^dag C(0) V
    VdagC0V = V.conj().T @ C_full_0 @ V
    # Apply phases: diag(e^{-i eps t}) @ VdagC0V @ diag(e^{i eps t})
    C_rot = phase[:, None] * VdagC0V * phase[None, :].conj()
    # Back-transform
    return V @ C_rot @ V.conj().T


def get_subsystem_correlator(C_full, NA):
    """Extract NA x NA physical correlator C_A from full 2N Nambu correlator."""
    return C_full[:NA, :NA]


