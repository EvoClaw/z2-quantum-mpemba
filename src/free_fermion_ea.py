def solve_BdG(HBdG):
    """
    Diagonalize the BdG Hamiltonian.
    Returns:
      eps: 2N eigenvalues (particle-hole symmetric: sum to zero)
      V: 2N x 2N unitary transformation matrix (columns = eigenvectors)
    
    For correlator evolution: C(t) = V diag(e^{-i eps_k t}) (V^dag C(0) V) diag(e^{i eps_k t}) V^dag
    """
    eps, V = eigh(HBdG)
    return eps, V


